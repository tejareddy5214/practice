public class Assessment00002 {
    public  static  void main(String[] args) {
        for(int i=5;i<=10;i++){
            int m=1;
            do{
                int v=m*i;
                System.out.println(i+" * "+m+" = "+v+" ");
                m++;
            }while(m<=10);
            System.out.println();
        }
    }
}
